# União de Conceitos
# Todas as estruturas podem ser usadas separadamente, mas em um programa "real",
# vamos unidos todas essas estruturas para criarmos os cenários que precisamos para resolver um problema.

#Exemplo

# Você quer saber se uma palavra contém a letra Y

palavra = "Python"
letraProcurada = "y"
for letra in palavra:
    if letra == letraProcurada:
        print("Essa palavra tem a letra",letraProcurada)
  